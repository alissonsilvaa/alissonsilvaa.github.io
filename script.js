// Função para copiar informações de contato
function copyToClipboard(text) {
    navigator.clipboard.writeText(text).then(() => {
        // Mostrar feedback visual
        const toast = document.createElement('div');
        toast.textContent = 'Copiado para a área de transferência!';
        toast.style.cssText = `
            position: fixed;
            top: 20px;
            right: 20px;
            background: #10b981;
            color: white;
            padding: 12px 24px;
            border-radius: 8px;
            z-index: 10000;
            animation: slideIn 0.3s ease;
        `;
        
        document.body.appendChild(toast);
        
        setTimeout(() => {
            toast.remove();
        }, 3000);
    });
}

// Adicionar funcionalidade de cópia aos contatos
document.querySelectorAll('.contact-details p').forEach(p => {
    p.style.cursor = 'pointer';
    p.title = 'Clique para copiar';
    
    p.addEventListener('click', () => {
        copyToClipboard(p.textContent);
    });
});

