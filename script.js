// Navegação mobile
const hamburger = document.querySelector('.hamburger');
const navMenu = document.querySelector('.nav-menu');

hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('active');
    navMenu.classList.toggle('active');
});

// Fechar menu ao clicar em um link
document.querySelectorAll('.nav-link').forEach(n => n.addEventListener('click', () => {
    hamburger.classList.remove('active');
    navMenu.classList.remove('active');
}));

// Smooth scroll para links internos
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Animação de scroll para timeline
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('animate');
        }
    });
}, observerOptions);

// Observar elementos da timeline
document.querySelectorAll('.timeline-item').forEach(item => {
    observer.observe(item);
});

// Observar cards de habilidades
document.querySelectorAll('.skill-card').forEach(card => {
    observer.observe(card);
});

// Observar cards de educação
document.querySelectorAll('.education-card').forEach(card => {
    observer.observe(card);
});

// Observar items de contato
document.querySelectorAll('.contact-item').forEach(item => {
    observer.observe(item);
});

// Efeito de typing no hero
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Aplicar efeito de typing quando a página carregar
window.addEventListener('load', () => {
    const heroName = document.querySelector('.hero-name');
    const heroRole = document.querySelector('.hero-role');
    
    if (heroName && heroRole) {
        const nameText = heroName.textContent;
        const roleText = heroRole.textContent;
        
        typeWriter(heroName, nameText, 80);
        
        setTimeout(() => {
            typeWriter(heroRole, roleText, 60);
        }, nameText.length * 80 + 500);
    }
});

